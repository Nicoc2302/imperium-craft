                1) Comment faire pour rejoindre Imperium ?
            </h2>
            Pour rejoindre le serveur, il vous suffit de télécharger le modpack ici :<br>
            Une fois le Modpack téléchargé, installez forge 1.7.10 ici en cliquant sur "installer win" :<br> 
