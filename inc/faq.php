            <h2>
                3) Comment obtenir l'apparence de la race que j'ai choisi ? 
            </h2>
                Pour changer d'apparence, Imperium-craft utilise le mod More Player Model de Noppes.<br>
                - Pour changer d'apaprence, choisissez d'abord en surfant sur le net un skin en rapport avec votre race qui fasse médiéval.<br>
                - Une fois le skin trouvé, rendez-vous en jeu et appuyez sur f12.<br>
                Cette action ouvre le menu MPM qui vous permettra de changer d'apparence.<br>
                - Une fois dans le menu, cliquez sur "Load Settings". Dans ce menu déroulant choissre votre race et votre sexe.<br>
            <ul>
                <li>pour les Humains, choisissez Human Male/Female puis load</li>

                <li>pour les Elfes, choisissez Elf Male/Female puis load</li>

                <li>pour les Naïades, choisissez Elf Male/Female puis load</li>

                <li>pour les Arkoudas, choisissez Human ou Orc puis load</li>

                <li>pour les Lupus choississez Wolf Male/Female puis load</li>

                <li>pour les Nagas, il faut rester dans le menu principale de MPM. une fois sur le menu, cliquez sur "Legs" a gauche de l'écran. Une fosi dans le menu Legs, cliquez plusieurs fois sur le premier bouton afin de tomber sur "Player Naga" ou tout simplement "Naga".</li>
            </ul>
            <br>
            Une fois terminer, faites "exit"' !<br>
            Il n'y a pas de skin préconsus dans MPM, il faut impérativemetn que vous en choisissiez un enligne et de thème médiéval.<br>
