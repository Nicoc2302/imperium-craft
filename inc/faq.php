                2) J'ai un écran bizarre quand je me connecte puis un léger freeze puis tout devient normal.
            </h2>
            Le serveur est équipé d'un ressource pack qui s'installe automatiquement lors de la connexion. Ceux qui le possèdent déjà n'y verront aucune différence tandis que les autres verront leur textures modifiées afin qu'ils aient le meilleur rendu possible des environements d'Imperium.
